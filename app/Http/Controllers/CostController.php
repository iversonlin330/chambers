     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        //
        $data = $request->all();

        for ($i = 0; $i < count($data['event_id']); $i++) {
            // 將每個索引位置的值組合成一個新的陣列
            if ($data['event_id']) {
                $result[] = [
                    'event_id' => $data['event_id'][$i],
                    'item' => $data['item'][$i],
                    'amount' => $data['amount'][$i],
                ];
            }
        }

        Cost::insert($result);

        redirect('');
    }

